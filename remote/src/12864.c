void send_command_to_ROM(u8 datu)
{
	u8 i;
	for(i=0;i<8;i++ )
	{
		Rom_SCK=0;
		delay_us(10);
		if(datu&0x80)Rom_IN = 1;
		else Rom_IN = 0;
		datu = datu<<1;
		Rom_SCK=1;
		delay_us(10);
	}
}
