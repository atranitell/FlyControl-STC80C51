static u8 get_data_from_ROM( )
{
		u8 i;
		u8 ret_data=0;
		for(i=0;i<8;i++)
		{
			Rom_OUT=1;
			Rom_SCK=0;
			//delay_us(1);
			ret_data=ret_data<<1;
			if( Rom_OUT )
			ret_data=ret_data+1;
			else
			ret_data=ret_data+0;
			Rom_SCK=1;
			//delay_us(1);
		}
		return(ret_data);
}
